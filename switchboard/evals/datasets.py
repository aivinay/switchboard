def cases_for_suite(suite: str) -> list[EvalCase]:
    if suite == "routing":
        return routing_cases()
    if suite == "tools":
        return tool_cases()
    if suite == "session":
        return session_cases()
    if suite == "real-smoke":
        return real_smoke_cases()
    if suite == "all":
        return [*routing_cases(), *tool_cases(), *session_cases()]
    raise ValueError(f"Unknown eval suite: {suite}")
